# === TEMPERATURE PLOT ===
def plot_temperature(dates, max_temps, min_temps):
    plt.figure(figsize=(10, 5))
    plt.plot(dates, max_temps, marker='o', color='orangered', label="Max Temp (Â°C)")
    plt.plot(dates, min_temps, marker='o', color='dodgerblue', label="Min Temp (Â°C)")
