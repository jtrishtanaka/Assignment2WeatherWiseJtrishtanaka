# === VISUALISE FORECAST ===
def visualise_forecast(data):
    try:
        days = data['weather']
        dates, max_temps, min_temps, rainfall = [], [], [], []

        for day in days:
            dates.append(day['date'])
            max_temps.append(int(day['maxtempC']))
            min_temps.append(int(day['mintempC']))
            rainfall.append(float(day['hourly'][4]['precipMM']))  # Midday reading

        vis_choice = pyip.inputMenu([
            "Temperature only ğŸŒ¡ï¸",
            "Rainfall only ğŸŒ§ï¸",
            "Both ğŸŒ¦ï¸"
        ], numbered=True, prompt="\nğŸ“Š What would you like to visualise?\n")

        if vis_choice == "Temperature only ğŸŒ¡ï¸":
            plot_temperature(dates, max_temps, min_temps)
        elif vis_choice == "Rainfall only ğŸŒ§ï¸":
            plot_rainfall(dates, rainfall)
        elif vis_choice == "Both ğŸŒ¦ï¸":
            plot_temperature(dates, max_temps, min_temps)
            plot_rainfall(dates, rainfall)

        show_motivational_quote("Trisha")

    except KeyError:
        print("âš ï¸ Forecast data is incomplete.")

# === PARSE NATURAL QUESTION ===
def parse_question(question):
    location_match = re.search(r"in ([a-zA-Z\s]+)", question)
    location = location_match.group(1).strip() if location_match else "Port Louis"

    day_offset = 0
    if "tomorrow" in question.lower():
        day_offset = 1
    elif "day after" in question.lower():
        day_offset = 2

    return location, day_offset

# === ANSWER WEATHER QUESTION ===
def answer_weather_question(question):
    location, day_offset = parse_question(question)
    data = fetch_weather(location)

    if not data:
        print("âŒ Could not retrieve data.")
        return

    if day_offset == 0:
        display_current_weather(data, location)
    else:
        try:
            day_data = data['weather'][day_offset]
            print(f"\nğŸ“… Forecast for {location.title()} on {day_data['date']}:")
            print(f"ğŸŒ¡ï¸ Max: {day_data['maxtempC']}Â°C | Min: {day_data['mintempC']}Â°C")
            print(f"ğŸŒ§ï¸ Condition: {day_data['hourly'][4]['weatherDesc'][0]['value']}")
            print(f"ğŸ’§ Rainfall: {day_data['hourly'][4]['precipMM']} mm")
            show_motivational_quote("Trisha")
        except IndexError:
            print("âš ï¸ That forecast isn't available yet.")

