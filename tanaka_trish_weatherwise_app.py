# === RAINFALL PLOT ===
def plot_rainfall(dates, rainfall):
    plt.figure(figsize=(10, 5))
    plt.bar(dates, rainfall, color='skyblue', edgecolor='navy')
    plt.title("ğŸŒ§ï¸ 3-Day Rainfall Forecast (Midday Readings)")
